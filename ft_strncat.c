#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int x;

	i = 0;
	x = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[x] != '\0' && n)
	{
		s1[i + x] = s2[x];
		x++;
		n--;
	}
	s1[i + x] = '\0';
	return (s1);
}
